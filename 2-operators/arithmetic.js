// Javascript has 6 kinds of operators
// 1. Arithmetic (+,-,*,/,%,**,++,--)
// 2. Assignment (=, +=, -=, *=, /=)
// 3. Comparison or Relational (<,>,<=,>=)
// 4. Equality (===, ==)
// 5. Ternary or conditional (condition ? value1 : value2)
// 6. Logical (!, ||, && )
// 7. Bitwise (|, &)

// 1. Arithmetic (+,-,*,/,%,**,++,--)
let x = 1;
let y = 2;
console.log('x', x, 'y', y);
let sum = x+y;
console.log('sum', sum);
let difference = x-y;
console.log('difference', difference);
let product = x*y;
console.log('product', product);
let quotient = x/y;
console.log('quotient', quotient);
let remainder = x%y;
console.log('remainder', remainder);
let power = x**y;
console.log('power', power);
// ++, --
console.log('x', x);
let pre_increment = ++x;
console.log('++x', pre_increment);
console.log('x', x);
let post_increment = x++;
console.log('x++', post_increment);
console.log('x', x);
console.log('y', y);
let pre_decrement = --y;
console.log('--y', pre_decrement);
console.log('y', y);
let post_decrement = y--;
console.log('y--', post_decrement);
console.log('y', y);
console.log('x', x, 'y', y);
